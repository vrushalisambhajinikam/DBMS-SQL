CREATE TABLE Bonus(
	WORKER_REF_ID INT,
    BONUS_AMT INT(10),
    BONUS_DATE DATETIME
		REFERENCES Worker(WORKER_ID)
        ON DELETE CASCADE
);

CREATE TABLE Title(
	WORKER_REF_ID INT,
    WORKER_TITLE CHAR(30),
    AFFECTED_FORM DATETIME
		REFERENCES Worker(WORKER_ID)
