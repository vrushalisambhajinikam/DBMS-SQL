CREATE TABLE Worker(
	WORKER_ID INT NOT NULL PRIMARY KEY AUTO_INCREMENT,
    WORKER_FNAME CHAR(25),
    WORKER_LNAME CHAR(25),
	SALARY INT(15),
    JOINING_DATE DATEtIME,
    DEPARTMENT CHAR(25)
);

